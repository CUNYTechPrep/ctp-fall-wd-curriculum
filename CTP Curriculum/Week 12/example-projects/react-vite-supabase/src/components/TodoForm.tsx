/**
 * REF: form-submission-handler
 *
 * ## Form Submission & Validation
 *
 * ### Client-Side Validation
 * - Title required and non-empty (trimmed)
 * - Max length enforced by HTML maxLength attribute
 * - Tags limited to 10 maximum
 * - Description optional but trimmed
 *
 * ### Supabase Insert Operation
 * ```typescript
 * const { data, error } = await supabase
 *   .from('todos')
 *   .insert({
 *     user_id,           // From auth
 *     title,             // Required
 *     description,       // Optional
 *     completed: false,  // Always new
 *     is_public,         // From checkbox
 *     tags,              // From array
 *     created_at: new Date().toISOString(),  // Server time
 *     updated_at: new Date().toISOString()
 *   })
 *   .select()           // Return inserted row
 *   .single()           // Expect exactly one
 * ```
 *
 * ### RLS Security Check
 * Database enforces: `auth.uid() = user_id`
 * - User cannot set user_id to someone else's ID
 * - Database will reject if doesn't match
 * - No way to bypass from client
 *
 * ### Error Handling
 * - RLS violations show as 403 Forbidden
 * - Network errors show timeout
 * - Validation errors show message
 * - All displayed to user in error state
 *
 * ### Success Flow
 * 1. Insert succeeds
 * 2. Clear form fields
 * 3. Call onSuccess callback
 * 4. Parent refetches todos list
 * 5. New todo appears in UI
 *
 * CLOSE: form-submission-handler
 */

/**
 * REF: controlled-input-pattern
 *
 * ## React Controlled Components
 *
 * ### What Are Controlled Components?
 * Input values stored in React state, not DOM
 * - User types → onChange fires → setState → input updates
 * - Single source of truth: state object
 * - Enables real-time validation
 * - Easy to reset or modify values
 *
 * ### Uncontrolled Alternative
 * ```typescript
 * // Uncontrolled - value stored in DOM
 * <input defaultValue="initial" />
 * const value = inputRef.current.value  // Accessed on submit
 * ```
 *
 * ### Controlled Pattern (This File)
 * ```typescript
 * // State holds value
 * const [title, setTitle] = useState('')
 *
 * // Input always reflects state
 * <input
 *   value={title}
 *   onChange={(e) => setTitle(e.target.value)}
 * />
 * ```
 *
 * ### Why Controlled?
 * - Validation as user types
 * - Reset button clears form
 * - Disable during submission
 * - Format/transform input
 * - Prevents invalid states
 *
 * CLOSE: controlled-input-pattern
 */

